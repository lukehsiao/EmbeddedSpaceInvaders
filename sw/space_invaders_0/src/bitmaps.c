/**
 * This function returns the color of the pixel to draw assuming we're
 * already looking inside the small tank array.
 *
 * @param row The row number of the pixel within the tank
 * @param col The column number of the pixel within the tank
 * @return the color value to draw at that pixel
 */
u32 getTankPixel(u32 row, u32 col) {
	u32 temp;
	temp = 0;
	temp = (TANK[row]>>(31-col)) & 0x00000001;
	if (temp != 0) {
		temp = GREEN;
	}
	else {
		temp = BLACK;
	}
	return temp;
}
/**
 * This function returns the color of the pixel to draw assuming we're
 * already looking inside the specified bunker. Note that this is note
 * very efficient, and may be optimized in the future.
 *
 * @param row The row number of the pixel within the given bunker
 * @param col The column number of the pixel within the given bunker
 * @param bunkerNumber	number of the bunker (0-3)
 * @param blockNumber	number of the block(0-11)
 * @return The pixels present in the given row
 */
u32 getBunkerPixel(u32 row, u8 bunkerNumber, u8 blockNumber) {
	u8 tempBlockErosion;
	u32 tempResult;
	u32 tempBase, erosionMask;

	tempBlockErosion = getBlockState(bunkerNumber, blockNumber);
	switch (blockNumber) {
		case 0:
			tempBase = (BUNKER_TOP_LEFT[row]);
			switch (tempBlockErosion) {
				case 0:
					erosionMask = 0xFFF00000;
					break;
				case 1:
					erosionMask = ((BUNKER_ERODE_01_LEFT[row] << 12));
					break;
				case 2:
					erosionMask = (BUNKER_ERODE_23_LEFT[row]) & 0xFFF00000;
					break;
				case 3:
					erosionMask = ((BUNKER_ERODE_23_LEFT[row] << 12));
					break;
				default:
					erosionMask = BLACK;
			}
			tempResult = tempBase & erosionMask;
			break;
		case 3:
			tempBase = (BUNKER_TOP_RIGHT[row]);
			switch (tempBlockErosion) {
				case 0:
					erosionMask = 0xFFF00000;
					break;
				case 1:
					erosionMask = ((BUNKER_ERODE_01_RIGHT[row] << 12));
					break;
				case 2:
					erosionMask = (BUNKER_ERODE_23_RIGHT[row]) & 0xFFF00000;
					break;
				case 3:
					erosionMask = ((BUNKER_ERODE_23_RIGHT[row] << 12));
					break;
				default:
					erosionMask = BLACK;
			}
			tempResult = tempBase & erosionMask;
			break;
		case 5:
			tempBase = (BUNKER_MIDDLE_LEFT[row]);
			switch (tempBlockErosion) {
				case 0:
					erosionMask = 0xFFF00000;
					break;
				case 1:
					erosionMask = ((BUNKER_ERODE_01_RIGHT[row] << 12));
					break;
				case 2:
					erosionMask = (BUNKER_ERODE_23_RIGHT[row]) & 0xFFF00000;
					break;
				case 3:
					erosionMask = ((BUNKER_ERODE_23_RIGHT[row] << 12));
					break;
				default:
					erosionMask = BLACK;
			}
			tempResult = tempBase & erosionMask;
			break;
		case 6:
			tempBase = (BUNKER_MIDDLE_RIGHT[row]);
			switch (tempBlockErosion) {
				case 0:
					erosionMask = 0xFFF00000;
					break;
				case 1:
					erosionMask = ((BUNKER_ERODE_01_RIGHT[row] << 12));
					break;
				case 2:
					erosionMask = (BUNKER_ERODE_23_RIGHT[row]) & 0xFFF00000;
					break;
				case 3:
					erosionMask = ((BUNKER_ERODE_23_RIGHT[row] << 12));
					break;
				default:
					erosionMask = BLACK;
			}
			tempResult = tempBase & erosionMask;
			break;
		case 9:
			return BLACK;	//blocks 9 and 10 are always black
		case 10:
			return BLACK;
		default:
			tempBase = 0xFFF00000;	//other blocks are all 1s
			switch (tempBlockErosion) {
				case 0:
					erosionMask = 0xFFF00000;
					break;
				case 1:
					erosionMask = ((BUNKER_ERODE_01_RIGHT[row] << 12));
					break;
				case 2:
					erosionMask = (BUNKER_ERODE_23_RIGHT[row]) & 0xFFF00000;
					break;
				case 3:
					erosionMask = ((BUNKER_ERODE_23_RIGHT[row] << 12));
					break;
				default:
					erosionMask = BLACK;
			}
			tempResult = tempBase & erosionMask;
			break;
	}
	return tempResult;
}

/**
 * This function calculates which alien array to return based on alien
 * number.
 *
 * @param alienNumber the number of the alien to display
 * @return a pointer to the 32x16 bit array of bitmap data.
 */
const u32* getAlienArray(u32 alienNumber) {
	//If alien is dead, return black
	if (getAlienStatus(alienNumber) == 0) {
		return ALIEN_DEAD;
	}

	// Otherwise...
	u8 tempGuise;
	tempGuise = getAlienGuise();
	if (alienNumber < 11) {
		if (tempGuise == 0) {
			return ALIEN1_IN;
		}
		else {
			return ALIEN1_OUT;
		}
	}
	else if (alienNumber < 33) {
		if (tempGuise == 0) {
			return ALIEN2_IN;
		}
		else {
			return ALIEN2_OUT;
		}
	}
	else {
		if (tempGuise == 0) {
			return ALIEN3_IN;
		}
		else {
			return ALIEN3_OUT;
		}
	}

}

